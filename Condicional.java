public class Condicional {
    public static void main(String[] args){
        int numeroIf = 0;
        if(numeroIf == 0){
            System.out.println("El numero es neutro");
        } else if(numeroIf < 0){
            System.out.println("El numero es negativo");
        } else {
            System.out.println("El numero es positivo");
        }
    }
}
